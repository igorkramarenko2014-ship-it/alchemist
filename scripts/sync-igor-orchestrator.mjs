#!/usr/bin/env node
/**
 * 1) Workspace `apps/*` + `packages/*` + `igor-orchestrator-meta.json` →
 *    `igor-orchestrator-packages.gen.ts`
 * 2) `igor-power-cells.json` → `igor-orchestrator-cells.gen.ts`
 *
 *   node scripts/sync-igor-orchestrator.mjs           # write both .gen.ts
 *   node scripts/sync-igor-orchestrator.mjs --check   # exit 1 if either stale
 *
 *   pnpm igor:sync
 *
 * IOM constraints (docs/iom.md): cell ceiling IOM_CELL_MAX (default 32),
 * every power-cell artifact path must exist as a file under packages/shared-engine/.
 */
import { existsSync, readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const DEFAULT_ROLE =
  "Monorepo workspace package — add an entry to packages/shared-engine/igor-orchestrator-meta.json for Igor role + canon anchors.";
const DEFAULT_ANCHORS = ["AGENTS.md"];

function findMonorepoRoot(startDir) {
  let dir = startDir;
  for (let i = 0; i < 16; i++) {
    const pj = join(dir, "package.json");
    if (existsSync(pj)) {
      try {
        const j = JSON.parse(readFileSync(pj, "utf8"));
        if (j.name === "alchemist" && Array.isArray(j.workspaces)) return dir;
      } catch {
        /* ignore */
      }
    }
    const parent = dirname(dir);
    if (parent === dir) break;
    dir = parent;
  }
  return null;
}

function listWorkspacePackageDirs(root) {
  const out = [];
  for (const top of ["apps", "packages"]) {
    const base = join(root, top);
    if (!existsSync(base)) continue;
    for (const ent of readdirSync(base, { withFileTypes: true })) {
      if (!ent.isDirectory()) continue;
      const rel = `${top}/${ent.name}`;
      if (existsSync(join(root, rel, "package.json"))) out.push(rel);
    }
  }
  return out.sort((a, b) => a.localeCompare(b));
}

function loadMeta(root) {
  const p = join(root, "packages", "shared-engine", "igor-orchestrator-meta.json");
  if (!existsSync(p)) return {};
  try {
    return JSON.parse(readFileSync(p, "utf8"));
  } catch {
    return {};
  }
}

function buildPackageRows(root) {
  const meta = loadMeta(root);
  const rows = [];
  for (const pathFromRepoRoot of listWorkspacePackageDirs(root)) {
    const pkgPath = join(root, pathFromRepoRoot, "package.json");
    let name = "";
    try {
      const j = JSON.parse(readFileSync(pkgPath, "utf8"));
      name = typeof j.name === "string" ? j.name : "";
    } catch {
      continue;
    }
    if (!name.startsWith("@alchemist/")) continue;
    const m = meta[pathFromRepoRoot];
    const role = m && typeof m.role === "string" ? m.role : DEFAULT_ROLE;
    let anchors = DEFAULT_ANCHORS;
    if (m && Array.isArray(m.canonAnchors) && m.canonAnchors.every((x) => typeof x === "string")) {
      anchors = m.canonAnchors;
    }
    rows.push({ packageName: name, pathFromRepoRoot, role, canonAnchors: anchors });
  }
  return rows;
}

function emitPackagesTs(rows) {
  const lines = [
    "/**",
    " * Generated by scripts/sync-igor-orchestrator.mjs from workspace package.json + igor-orchestrator-meta.json.",
    " * Do not hand-edit — run `pnpm igor:sync`.",
    " */",
    "/* eslint-disable */",
    "",
    "export const IGOR_ORCHESTRATOR_PACKAGES_GEN = [",
  ];
  for (const r of rows) {
    const anchors = r.canonAnchors.map((a) => JSON.stringify(a)).join(", ");
    lines.push("  {");
    lines.push(`    packageName: ${JSON.stringify(r.packageName)},`);
    lines.push(`    pathFromRepoRoot: ${JSON.stringify(r.pathFromRepoRoot)},`);
    lines.push(`    role: ${JSON.stringify(r.role)},`);
    lines.push(`    canonAnchors: [${anchors}] as const,`);
    lines.push("  },");
  }
  lines.push("] as const;");
  lines.push("");
  return lines.join("\n");
}

function loadPowerCells(engineDir) {
  const p = join(engineDir, "igor-power-cells.json");
  if (!existsSync(p)) {
    throw new Error(`[igor:sync] missing ${p} — create it or restore from repo`);
  }
  let raw;
  try {
    raw = JSON.parse(readFileSync(p, "utf8"));
  } catch (e) {
    throw new Error(`[igor:sync] invalid JSON: ${p} (${e.message})`);
  }
  if (!Array.isArray(raw) || raw.length === 0) {
    throw new Error(`[igor:sync] igor-power-cells.json must be a non-empty array`);
  }
  const ids = new Set();
  const cells = [];
  for (let i = 0; i < raw.length; i++) {
    const c = raw[i];
    if (!c || typeof c !== "object") throw new Error(`[igor:sync] power cell[${i}] not an object`);
    const id = c.id;
    const responsibility = c.responsibility;
    if (typeof id !== "string" || !id.trim()) throw new Error(`[igor:sync] power cell[${i}] bad id`);
    if (ids.has(id)) throw new Error(`[igor:sync] duplicate power cell id: ${id}`);
    ids.add(id);
    if (typeof responsibility !== "string" || !responsibility.trim()) {
      throw new Error(`[igor:sync] power cell ${id}: missing responsibility`);
    }
    if (!Array.isArray(c.artifacts) || !c.artifacts.every((x) => typeof x === "string" && x.trim())) {
      throw new Error(`[igor:sync] power cell ${id}: artifacts must be non-empty strings array`);
    }
    const tier = c.tier;
    if (tier !== undefined && typeof tier !== "string") {
      throw new Error(`[igor:sync] power cell ${id}: tier must be a string`);
    }

    cells.push({
      id: id.trim(),
      responsibility: responsibility.trim(),
      artifacts: c.artifacts.map((x) => x.trim()),
      tier: typeof tier === "string" ? tier.trim() : undefined,
    });
  }
  return cells;
}

/** @param {{ id: string; responsibility: string; artifacts: string[] }[]} cells */
function assertIomPowerCellConstraints(cells, engineDir) {
  const maxRaw = process.env.IOM_CELL_MAX;
  const max = maxRaw !== undefined && maxRaw !== "" ? Number.parseInt(String(maxRaw), 10) : 32;
  if (!Number.isFinite(max) || max < 1) {
    throw new Error("[igor:sync] IOM_CELL_MAX must be a positive integer");
  }
  if (cells.length > max) {
    throw new Error(
      `[igor:sync] IOM: cell count ${cells.length} exceeds ceiling ${max}. Retire or merge, or raise IOM_CELL_MAX with intent (see docs/iom.md).`,
    );
  }
  for (const c of cells) {
    for (const rel of c.artifacts) {
      const abs = join(engineDir, rel);
      if (!existsSync(abs)) {
        throw new Error(
          `[igor:sync] IOM: cell "${c.id}" artifact "${rel}" not found under packages/shared-engine/. Ship the file or fix the path.`,
        );
      }
      const st = statSync(abs);
      if (!st.isFile()) {
        throw new Error(`[igor:sync] IOM: cell "${c.id}" artifact "${rel}" is not a file.`);
      }
    }
  }
}

function emitCellsTs(cells) {
  const lines = [
    "/**",
    " * Generated by scripts/sync-igor-orchestrator.mjs from igor-power-cells.json.",
    " * Edit the JSON — run `pnpm igor:sync`. Do not hand-edit this file.",
    " */",
    "/* eslint-disable */",
    "",
    "export const IGOR_SHARED_ENGINE_POWER_CELLS_GEN = [",
  ];
  for (const c of cells) {
    const arts = c.artifacts.map((a) => JSON.stringify(a)).join(", ");
    lines.push("  {");
    lines.push(`    id: ${JSON.stringify(c.id)},`);
    lines.push(`    responsibility: ${JSON.stringify(c.responsibility)},`);
    lines.push(`    artifacts: [${arts}] as const,`);
    if (c.tier) {
      lines.push(`    tier: ${JSON.stringify(c.tier)},`);
    }
    lines.push("  },");
  }
  lines.push("] as const;");
  lines.push("");
  return lines.join("\n");
}

const here = dirname(fileURLToPath(import.meta.url));
const root = findMonorepoRoot(here) ?? findMonorepoRoot(process.cwd());
if (!root) {
  console.error("[igor:sync] monorepo root not found");
  process.exit(1);
}

const engineDir = join(root, "packages", "shared-engine");
const pkgPath = join(engineDir, "igor-orchestrator-packages.gen.ts");
const cellPath = join(engineDir, "igor-orchestrator-cells.gen.ts");

let cells;
try {
  cells = loadPowerCells(engineDir);
  assertIomPowerCellConstraints(cells, engineDir);
} catch (e) {
  console.error(e.message || e);
  process.exit(1);
}

const pkgContent = emitPackagesTs(buildPackageRows(root));
const cellContent = emitCellsTs(cells);
const check = process.argv.includes("--check");

if (check) {
  let bad = false;
  if (!existsSync(pkgPath)) {
    console.error("[igor:sync] --check: missing", pkgPath, "— run pnpm igor:sync");
    bad = true;
  } else if (readFileSync(pkgPath, "utf8") !== pkgContent) {
    console.error("[igor:sync] --check: stale", pkgPath, "— run pnpm igor:sync");
    bad = true;
  }
  if (!existsSync(cellPath)) {
    console.error("[igor:sync] --check: missing", cellPath, "— run pnpm igor:sync");
    bad = true;
  } else if (readFileSync(cellPath, "utf8") !== cellContent) {
    console.error("[igor:sync] --check: stale", cellPath, "— run pnpm igor:sync");
    bad = true;
  }
  if (bad) process.exit(1);
  console.error("[igor:sync] --check: OK (packages + power cells)");
  process.exit(0);
}

writeFileSync(pkgPath, pkgContent, "utf8");
writeFileSync(cellPath, cellContent, "utf8");
console.error("[igor:sync] wrote", pkgPath);
console.error("[igor:sync] wrote", cellPath);
